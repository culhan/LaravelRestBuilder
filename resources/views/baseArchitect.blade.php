      function arrayUnique(array) {
          var a = array.concat();
          for(var i=0; i<a.length; ++i) {
              for(var j=i+1; j<a.length; ++j) {
                  if(a[i] === a[j])
                      a.splice(j--, 1);
              }
          }

          return a;
      }

      function array_flip( trans )
      {
          var key, tmp_ar = {};

          for ( key in trans )
          {
              if ( trans.hasOwnProperty( key ) )
              {
                  tmp_ar[trans[key]] = key;
              }
          }

          return tmp_ar;
